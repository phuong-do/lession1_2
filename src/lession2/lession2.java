package lession2;

import java.util.Scanner;

public class lession2 {
    public static void main (String[]args) {
        int a, b, c;
        Scanner Scanner = new Scanner(System.in);

        a = Scanner.nextInt();
        b = Scanner.nextInt();
        c = a + b;
        System.out.println("a + b =" + c);
        c = a * b;
        System.out.println("a * b =" + c);
        c = a - b;
        System.out.println("a - b =" + c);
        c = a / b;
        System.out.println("a / b =" + c);
        c = a % b;
        System.out.println("a % b =" + c);






    }

}
